// https://leetcode.com/problems/maximum-population-year/

function maximumPopulation(logs) {
    let populations = new Map()
    let startYear = 10 ** 8
    let endYear = 0

    for (const log of logs) {
        startYear = Math.min(startYear, log[0])
        endYear = Math.max(endYear, log[1] - 1)
        for (let year = log[0]; year < log[1]; year++) {
            populations.set(year, (populations.get(year) || 0) + 1)
        }
    }
    let maxYear = startYear
    for (let year = startYear + 1; year <= endYear; year++) {
        if ((populations.get(year) || 0) > populations.get(maxYear)) {
            maxYear = year
        }
    }

    return maxYear
}


